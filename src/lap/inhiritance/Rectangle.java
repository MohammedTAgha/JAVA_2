package lap.inhiritance;

public class Rectangle {
    private double hieght;
    private double wieght;
    public Rectangle(double hieght, double wieght) {
        this.hieght = hieght;
        this.wieght = wieght;
    }
    public double getArea(){
        return this.hieght*this.wieght;
    }




}
